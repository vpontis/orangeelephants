#define true 1

/** \brief The KISS-C equivalent for the boolean value true.
*/
#define TRUE 1

/** \brief The KISS-C equivalent for the boolean value false.
*/
#define false 0

/** \brief The KISS-C equivalent for the boolean value false.
*/
#define FALSE 0
